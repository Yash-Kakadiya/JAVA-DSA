import java.util.*;

public class DataTypesJava {
    public static void main(String args[]) {

        // data types
        byte y = 8;
        System.out.println(y);

        char c = 'a';
        System.out.println(c);

        short x = 240;
        System.out.println(x);
        
        boolean var = false;
        System.out.println(var);

        int num = 77;
        System.out.println(num);
        // long

        float pi = 3;
        System.out.println(pi);
        // double

        System.out.println("---------------------");

    }
}